     */
    public function update(Request $request, $id)
    {
         $login_user=Auth::user()->id;
         $arra= "";

         $arra=$login_user;

        foreach ($request->members as $value) {
            
            $arra.=",".$value;
        }

        Group_Member::where('group_id',$id)->update([

            'member_id'=>$arra,
        ]);

        return redirect('groups');
    }

